#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    [tableView deselectRowAtIndexPath:indexPath animated:nil];
    if (indexPath.section == 0 && indexPath.row == 0)
    {
        SnoozeTimeSelectController *controller = [[[SnoozeTimeSelectController alloc] initWithStyle:UITableViewStyleGrouped] autorelease];
        controller.selectAlarmNo = self.selectAlarmNo;
        [self.navigationController pushViewController:controller animated:YES];
    }
}

